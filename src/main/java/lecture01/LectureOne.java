package lecture01;

/**
 * This class demonstrates a simple Java program with a method that prints 
 * "Hello World!" to the console.
 * 
 * <p>This class introduces the basic structure of a Java program and shows 
 * how to create and call a method.</p>
 * 
 * @author Andreas Schneider
 */
public class LectureOne
{

    /**
     * The run method prints the message "Hello World!" to the console.
     * 
     * <p>This method demonstrates how to print a message using the 
     * System.out.println() function in Java.</p>
     */
    public static void run()
    {
        // Print the message "Hello World!" to the console
        System.out.println("Hello World!");
    }
}